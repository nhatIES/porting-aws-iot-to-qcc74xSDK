void itm_printf(const char *format, ...)
{
    char buffer[128];
    va_list args;

    va_start(args, format);
    vsnprintf(buffer, sizeof(buffer), format, args);
    va_end(args);

    for (char *ptr = buffer; *ptr != '\0'; ptr++)
        ITM_SendChar(*ptr);
}

void *memcpy(void *dest, const void *src, unsigned int len)
{
	__asm__ volatile (
	    /* Initialize local variables. */
	    "mov r0, %[dest]              \n"
	    "mov r1, %[src]               \n"
	    "mov r2, %[len]               \n"

	    /* Nothing to do if length is 0. */
	    "cmp r2, #0                   \n"
	    "beq 6f                       \n"

	    /* Handle unaligned destination address. */
	    "ands r3, r0, #3              \n"
	    "beq 2f                       \n"

	    /* Byte copy until the destination address is aligned. */
	    "1:                           \n"
	    "ldrb r3, [r1], #1            \n"
	    "strb r3, [r0], #1            \n"
	    "subs r2, r2, #1              \n"
	    "beq 6f                       \n"
	    "ands r3, r0, #3              \n"
	    "bne 1b                       \n"

	    /* Now handle word copy if length >= 4 */
	    "2:                           \n"
	    "cmp r2, #4                   \n"
	    "blt 4f                       \n"

	    "3:                           \n"
	    "ldr r3, [r1], #4             \n"
	    "str r3, [r0], #4             \n"
	    "subs r2, r2, #4              \n"
	    "cmp r2, #4                   \n"
	    "bge 3b                       \n"

	    /* Handle remaining bytes (if any). */
	    "4:                           \n"
	    "ands r3, r2, #3              \n"
	    "beq 6f                       \n"
	    "5:                           \n"
	    "ldrb r3, [r1], #1            \n"
	    "strb r3, [r0], #1            \n"
	    "subs r2, r2, #1              \n"
	    "bne 5b                       \n"

	    /* Done. */
	    "6:                           \n"
	    :
	    : [dest] "r" (dest), [src] "r" (src), [len] "r" (len)
	    : "r0", "r1", "r2", "r3", "memory"
	);

	return dest;
}

